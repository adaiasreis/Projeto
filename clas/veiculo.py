#Classe de veiculos
class Veiculo:
    def __init__ (self, id, marca, modelo, cor, categ, anoFab, placa, chassi, renavam, capPassag, portas, potencia, cilindradas):
        self.id = id
        self.marca = marca
        self.modelo = modelo
        self.cor = cor
        self.categ = categ
        self.anoFab = anoFab
        self.placa = placa
        self.chassi = chassi
        self.renavam = renavam
        self.capPassag = capPassag
        self.portas = portas
        self.potencia = potencia
        self.cilindradas = cilindradas